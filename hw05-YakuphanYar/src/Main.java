/*
UML nedir? En yaygın kullanılan UML nedir? İşlevleri ile yazınız.

UML (Unified Modelling Language, Birleşik Modelleme Dili) yazılım modellenmesi ve planlanması için kullanılan
standart bir dildir.
UML, yazılım ağırlıklı bir sistemi ve bu sistemin parçalarını gözde canlandırmak, belirtmek, kurmak ve belgelemek
için kullanılabilir. Kurumsal bilgi sistemlerinden, dağıtımlı ağ-tabanlı uygulamalara ve gerçek zamanlı gömülü
sistemlere kadar birçok sistemi modellemek için uygun bir dildir.

Yapısal Diyagramlar
Sınıf (Class) diyagramı, sistemin yapısını anlatmak için sistemde var olan sınıfları, sınıfların özelliklerini
ve sınıflar arası ilişkileri kullanır. Nesneye yönelik sistemleri modellemede kullanılan en yaygın diyagramdır.

Davranış Diyagramları
Kullanım Senaryosu (Use-Case) diyagramı, modellenen sistem tarafından sağlanan işlevselliği sistemde yer alan
aktörleri, aktörlerin sahip olduğu kullanım senaryolarını ve bu senaryolar arasındaki bağımlılıkları göstererek
açıklar.

 */

